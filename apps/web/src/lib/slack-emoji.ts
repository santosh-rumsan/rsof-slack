/**
 * Replaces Slack emoji shortcodes (:name:) in a string with their Unicode equivalents.
 * Unknown codes are left as-is.
 */
export function renderSlackEmoji(text: string): string {
  return text.replace(/:([a-z0-9_+-]+):/gi, (match, name: string) => {
    return EMOJI_MAP[name.toLowerCase()] ?? match;
  });
}
